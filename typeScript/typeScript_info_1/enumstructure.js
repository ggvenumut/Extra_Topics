var Colors;
(function (Colors) {
    Colors[Colors["red"] = 0] = "red";
    Colors[Colors["blue"] = 1] = "blue";
    Colors[Colors["green"] = 2] = "green";
})(Colors || (Colors = {}));
;
console.log(Colors.blue);
var Days;
